    private void btn_igualMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_igualMouseClicked
        numero2=Double.valueOf(txtEntrada.getText());
        double resultado=0;
        switch (operacion){
            case "+": resultado = numero1 + numero2;
                      break;
            case "-": resultado = numero1 - numero2;
                      break;
            case "*": resultado = numero1 * numero2;
                      break;
            case "/": if(numero2==0){
                        txtEntrada.setText("No permitido");
                        return;
                      }else{
                        resultado = numero1 / numero2;
                      }
                      break;
                      
            default: break;
        }
        txtEntrada.setText(String.valueOf(resultado));
        operacion="";
        decimal=false;// TODO add your handling code here:
    }//GEN-LAST:event_btn_igualMouseClicked

